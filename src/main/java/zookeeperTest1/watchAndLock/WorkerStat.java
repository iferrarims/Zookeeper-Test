package zookeeperTest1.watchAndLock;
public class WorkerStat {
    	
    public int start, runs,threads;
    public long setterTime, getterTime;
    public long createTime, deleteTime;
    public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	} 
	public int getRuns() {
		return runs;
	}
	public void setRuns(int runs) {
		this.runs = runs;
	}
	public int getThreads() {
		return threads;
	}
	public void setThreads(int threads) {
		this.threads = threads;
	}
	public long getSetterTime() {
		return setterTime;
	}
	public void setSetterTime(long setterTime) {
		this.setterTime = setterTime;
	}
	public long getGetterTime() {
		return getterTime;
	}
	public void setGetterTime(long getterTime) {
		this.getterTime = getterTime;
	}
	public long getCreateTime() {
		return createTime;
	}
	public void setCreateTime(long createTime) {
		this.createTime = createTime;
	}
	public long getDeleteTime() {
		return deleteTime;
	}
	public void setDeleteTime(long deleteTime) {
		this.deleteTime = deleteTime;
	}
	public WorkerStat() {
        start = runs = 0;
        setterTime = getterTime = 0;
        createTime=deleteTime=0;
    }
    }